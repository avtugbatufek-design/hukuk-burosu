const I18N_KEY = "site-lang";

function applyTranslations(lang) {
  const dict = translations[lang] || translations.tr;

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (key in dict) el.innerHTML = dict[key];
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.getAttribute("data-i18n-placeholder");
    if (key in dict) el.setAttribute("placeholder", dict[key]);
  });

  document.documentElement.setAttribute("lang", lang);

  document.querySelectorAll(".lang-switch button").forEach((btn) => {
    btn.classList.toggle("is-active", btn.dataset.lang === lang);
    btn.setAttribute("aria-pressed", btn.dataset.lang === lang);
  });
}

function setLang(lang) {
  if (!translations[lang]) lang = "tr";
  localStorage.setItem(I18N_KEY, lang);
  applyTranslations(lang);
}

function getLang() {
  const saved = localStorage.getItem(I18N_KEY);
  if (saved && translations[saved]) return saved;
  // Tarayıcı diline göre ilk tahmin
  return (navigator.language || "tr").toLowerCase().startsWith("tr") ? "tr" : "en";
}

// Sayfa yüklenince uygula + butonları bağla
document.addEventListener("DOMContentLoaded", () => {
  setLang(getLang());
  document.querySelectorAll(".lang-switch button").forEach((btn) => {
    btn.addEventListener("click", () => setLang(btn.dataset.lang));
  });
});
